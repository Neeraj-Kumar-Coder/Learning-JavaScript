// Printing colors in console
console.log("%c%s", "background: yellow; color: red; font-size: 24px; padding: 0.5rem 1rem; border-radius: 0.5rem;", "Warning");

/**
 * The %c tells the first argument is the CSS
 * The %s stands for the string that is needed to be styled
 * We can write multiple strings with their corresponding CSS
 */