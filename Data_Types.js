// Primitive Data Types

// String
let myName = "Neeraj";
console.log(myName);
console.log(typeof myName);

// Number
let age = 20;
console.log(age);
console.log(typeof age);

// Boolean
let isHappy = true;
console.log(isHappy);
console.log(typeof isHappy);

// Null
let nullVar = null;
console.log(nullVar);
console.log(typeof nullVar);

// Undefined
let undVar = undefined;
console.log(undVar);
console.log(typeof undVar);

// Referenced Data Types

// Array
let myArr = ['Neeraj', 20, true];
console.log(myArr);
console.log(typeof myArr);

// Object Literals
let myMarks = {
    English: 50,
    C: 100
};
console.log(myMarks);
console.log(typeof myMarks);

// Functions
function getName() {
    ;
}
console.log(typeof getName);

// Date
let date = new Date();
console.log(date);
console.log(typeof date);